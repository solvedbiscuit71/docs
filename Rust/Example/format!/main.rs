fn main() {
    println!(
        "This is {0} programming {lang}. {0} is a static type {lang}.",
        "rust",
        lang = "language"
    );

    println!("72 in binary is {:b}", 72);
    println!("Round off by 2 of 3.147 is {:.2}", 3.147);

    /*
     * similar to println!() they are,
     * 1. print!()
     * 2. format!()
     * 3. write!()
     */
}
